            `‚Ä¢ Ë∞ÉÊï¥ÁöÑÂ∏ÉÂ±Ä‰ΩçÁΩÆ\n\n` +
            `Á°ÆÂÆöË¶ÅÁªßÁª≠ÂêåÊ≠•ÂêóÔºüÊ≠§Êìç‰ΩúÊó†Ê≥ïÊí§ÈîÄ„ÄÇ`
          )
          
          if (!confirmOverride) {
            return
          }
        }

        const updateInfo = await checkTemplateUpdate(layout.id)

        if (!updateInfo.hasUpdate) {
          alert('Â∑≤ÊòØÊúÄÊñ∞ÁâàÊú¨ÔºÅ')
          return
        }

        const confirmMessage = layout.isModified
          ? `ÂèëÁé∞Êñ∞ÁâàÊú¨ v${updateInfo.latestVersion}ÔºåÂêåÊ≠•ÂêéÂ∞ÜË¶ÜÁõñ‰Ω†ÁöÑÊîπÂä®„ÄÇ\n\nÁ°ÆÂÆöË¶ÅÁ´ãÂç≥ÂêåÊ≠•Êõ¥Êñ∞ÂêóÔºü`
          : `ÂèëÁé∞Êñ∞ÁâàÊú¨ v${updateInfo.latestVersion}ÔºåÊòØÂê¶Á´ãÂç≥ÂêåÊ≠•Êõ¥Êñ∞Ôºü`

        if (await showConfirm(confirmMessage)) {
          const success = await syncTemplateUpdate(layout.id)
          if (success) {
            alert(`Â∑≤ÊàêÂäüÊõ¥Êñ∞Âà∞ v${updateInfo.latestVersion}`)
          } else {
            alert('Êõ¥Êñ∞Â§±Ë¥•ÔºåËØ∑Á®çÂêéÈáçËØï')
          }
        }
      } catch (error) {
        console.error('Ê£ÄÊü•Êõ¥Êñ∞Â§±Ë¥•:', error)
        alert('Ê£ÄÊü•Êõ¥Êñ∞Â§±Ë¥•')
      }
    }

    // ÂàÜ‰∫´Â∏ÉÂ±Ä
    const handleShareLayout = async (layout, event) => {
      event.stopPropagation()

      if (!layout.websites || layout.websites.length === 0) {
        alert('ËØ•Â∏ÉÂ±ÄÊ≤°ÊúâÁΩëÁ´ôÔºåÊó†Ê≥ïÂàÜ‰∫´')
